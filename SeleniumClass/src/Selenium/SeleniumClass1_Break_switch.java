package Selenium;

public class SeleniumClass1_Break_switch {

	public static void main(String[] args) {
		int age = 20;
		
		switch(age) {
		
		case 20:
			System.out.println("Your age is 1");
			break;
			
		case 2:
			System.out.println("Your age is 2");
			break;
			
		default:
			System.out.println("Don't know your age");
			
		}

	}

}
